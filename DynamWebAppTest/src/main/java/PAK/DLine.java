package PAK;

public class DLine {
	private String desc, url;

	public DLine() {
		super();
	}

	public DLine(String desc, String url) {
		super();
		this.desc = desc;
		this.url = url;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

}
